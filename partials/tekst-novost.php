		Udruženje studenata elektrotehnike Evrope, EESTEC LC Sarajevo 7. i 8. novembra ove godine organizuje osmi po redu sajam zapošljavanja za studente i diplomce tehničko-tehnoloških fakulteta i ekonomije "JobFAIR ‘16 - Iskoristi svoju šansu!". Organizatori Sajma još jednom dokazuju da u Bosni i Hercegovini postoje mladi koji ne čekaju prilike, nego ih stvaraju sami!
		<br>
		<br>
		Osnovni cilj JobFAIR-a je direktan kontakt između studenata i poslodavaca. Tokom dva dana trajanja Sajma posjetioci će biti u mogućnosti da razgovaraju sa predstavnicima kompanija,  informišu se o tome šta poslodavci očekuju od njih te koje vještine su poželjne za njihovu struku. Na osnovu stečenih informacija, studenti i diplomci mogu dalje raditi na usvršavanju ličnog profila.
		<br>
		<br>
		Ovaj projekat direktno stimuliše zapošljavanje mladih u uspješnim domaćim kompanijama. Također, kroz različita predavanja i savjete profesionalaca pruža se podrška mladima koji imaju ambicije da započnu sopstveni biznis. Osim samog zapošljavanja, projekat ima veliki značaj sa aspekta informisanja mladih. Posjetioci Sajma će imati priliku upoznati se sa mogućnostima koje pruža naša privreda, što će im pomoći u izboru budućeg profesionalnog usmjerenja.
		
